public class Pelota {
    public int x;
    public int y;
    public int veloX;
    public int veloY;
    public int ladoPelota;
   
    public Pelota(int x, int y, int veloX, int veloY, int ladoPelota) {
        this.x = x;
        this.y = y; 
        this.veloX = veloX;
        this.veloY = veloY;
        this.ladoPelota= ladoPelota;
    }
}
